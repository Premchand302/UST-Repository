package assignment;

public class math {
	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		int sum = a + b;
		int diff = a - b;
		int product = a * b;
		double quotient = (double) a / b;
		System.out.println("Sum = " + sum);
		System.out.println("Difference = " + diff);
		System.out.println("Product = " + product);
		System.out.println("Quotient = " + quotient);
	}
}
